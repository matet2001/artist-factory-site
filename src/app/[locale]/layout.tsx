import type { Metadata } from 'next'
import './globals.css'
import { NextIntlClientProvider, hasLocale } from 'next-intl'
import { notFound } from 'next/navigation'
import { routing } from '@/i18n/routing'

import { Sora, Space_Mono } from 'next/font/google'
import Header from '@/components/common/header'
import { Toaster } from 'sonner'

const sora = Sora({
    variable: '--font-sora',
    subsets: ['latin'],
})

const spaceMono = Space_Mono({
    variable: '--font-space-mono',
    subsets: ['latin'],
    weight: '400',
})

export const metadata: Metadata = {
    title: 'Artist Factory',
    description: 'Generated by create next app',
}

export default async function LocaleLayout({
    children,
    params,
}: {
    children: React.ReactNode
    params: Promise<{ locale: string }>
}) {
    // Ensure that the incoming `locale` is valid
    const { locale } = await params
    if (!hasLocale(routing.locales, locale)) {
        notFound()
    }

    return (
        <html className="overflow-x-hidden scroll-smooth" lang={locale}>
            <body
                className={`${sora.variable} font-main antialiased text-foreground bg-background overflow-x-hidden`}
            >
                <NextIntlClientProvider>
                    <div className="min-h-dvh flex flex-col isolate">
                        <Header />

                        <main className="flex-1 w-full max-w-screen overflow-x-hidden">
                            {children}
                        </main>

                        <Toaster position="top-center" richColors closeButton />
                        {/* <Footer /> */}
                        {/* <Analytics /> */}
                    </div>
                </NextIntlClientProvider>
            </body>
        </html>
    )
}
